<h1>Kewajikan</h1>

<p>Pembelian karcis di Kantor Balai Besar TNKS Sungai Penuh, Pusat informasi R10 Kersik Tuo Kabupaten Kerinci
   Provinsi Jambi untuk pendakian melalui pintu masuk Kersik Tuo sedangkan pendakian yang melalui pintu masuk
   Camping Ground Bangun Rejo Kabupaten Solok Selatan Provinsi Sumatera Barat menggunakan Karcis dan SIMAKSI.
</p>

<h2>Pengurusan SIMAKSI</h2>
<p>Pengurusan SIMAKSI dilakukan secara online dan langsung di:</p>
<ul>
   <li>Kantor BBTNKS di Sungai Penuh</li>
   <li>Kantor Bidang Pengelolaan TN Wilayah II Sumatera Barat di Padang</li>
   <li>Kantor Seksi Pengelolaan TN Wilayah IV Sangir</li>
</ul>
<p>Pengurusan bisa dilakukan 2 bulan hingga 1 hari sebelum pendakian.</p>

<h2>Batas Waktu Pendakian</h2>
<ul>
   <li>Pos R10 Kersik Tuo Kabupaten Kerinci Provinsi Jambi: 2 hari</li>
   <li>Camping Ground Bangun Rejo Kabupaten Solok Selatan Provinsi Sumatera Barat: 4 hari</li>
</ul>
<p>Apabila merencanakan lebih dari batas waktu tersebut, wajib memberitahukan di awal keberangkatan dan dikenai
   biaya kelipatannya.</p>

<h2>Prosedur Pendakian</h2>
<ul>
   <li>Pendaki wajib memberitahukan rencana tanggal dan lokasi turun/kembali serta melaporkan kepada petugas di
      pintu masuk.</li>
   <li>Tanggung jawab dan keselamatan pendaki menjadi tanggung jawab pribadi dan tidak akan menuntut pihak
      pengelola (Balai Besar TNKS), pemandu, porter, pemerintah, dan pemerintah daerah.</li>
   <li>Pendaki yang naik dari pintu masuk Kersik Tuo dan turun melalui pintu masuk Camping Ground Bangun Rejo
      Kabupaten Solok Selatan wajib menggunakan SIMAKSI.</li>
   <li>Apabila pendaki melanggar kelebihan hari dalam mendaki akan dikenakan sanksi, kecuali dalam keadaan
      darurat dan telah dilaporkan kepada petugas.</li>
   <li>Jumlah pendaki yang diizinkan naik maksimum 46 orang/hari khusus jalur pendakian melalui Camping Ground
      Bangun Rejo Kabupaten Solok Selatan Provinsi Sumatera Barat.</li>
   <li>Setiap SIMAKSI pendakian dikeluarkan untuk grup pendaki dengan jumlah minimum 3 orang dan maksimum 10
      orang pendaki.</li>
   <li>Untuk mengurangi risiko kecelakaan, diwajibkan menggunakan jasa pemanduan dari Sekretariat Bersama
      Pemandu Gunung Kerinci Kabupaten Solok Selatan yang telah mendapat rekomendasi dari Balai Besar TNKS.
   </li>
   <li>Proses pengambilan SIMAKSI bisa dilakukan pada hari dan jam kerja.</li>
   <li>Semua calon pendaki wajib menyerahkan fotokopi Kartu Identitas yang masih berlaku seperti: SIM, KTA,
      KTP, Paspor, atau Kartu Pelajar serta Surat Keterangan Sehat dokter (asli).</li>
   <li>Calon pendaki yang berumur kurang dari 17 tahun wajib menyerahkan Surat Izin Orang Tua/Wali yang
      ditandatangani di atas materai Rp. 10.000 serta dilampirkan fotokopi KTP Orang Tua/Wali yang masih
      berlaku.</li>
   <li>Bagi Warga Negara Asing (WNA) yang akan mendaki diwajibkan untuk menggunakan jasa pemanduan yang telah
      terdaftar di Balai Besar TNKS.</li>
   <li>Berbadan sehat pada saat melakukan pendakian dengan menunjukkan Surat Keterangan Dokter (asli) pada
      pintu masuk dan pada saat mengurus SIMAKSI.</li>
   <li>Masuk jalur pendakian antara pukul 07.30 s/d 15.00 WIB dan mendaki pada jalur yang sudah
      ditentukan/jalur resmi.</li>
   <li>Mematuhi semua rambu-rambu dan informasi keselamatan yang ada di sepanjang jalur pendakian.</li>
   <li>Melakukan evakuasi mandiri terhadap diri dan rekannya yang sakit sebelum mendapatkan bantuan dari
      petugas.</li>
   <li>Wajib menggunakan dan membawa jasa Pemandu/Porter yang telah terdaftar di Balai Besar TNKS.</li>
   <li>Memakai dan membawa perlengkapan standar pendakian gunung serta perbekalan pendakian yang cukup.</li>
   <li>Mengisi form identitas diri dan isian barang bawaan yang menghasilkan sampah.</li>
   <li>Membawa trash bag kantong sampah dan membawa sampah bawaannya ke luar kawasan Taman Nasional.</li>
   <li>Memprioritaskan penanganan bagi wanita yang sedang menstruasi, utamanya segera membawa turun korban
      tersebut apabila sudah menderita sakit.</li>
   <li>Membawa kelengkapan P3K standar, dan survival kit standar.</li>
   <li>Menjaga norma agama, norma susila dan kearifan lokal.</li>
   <li>Mengikuti jalur pendakian yang sudah ditetapkan Balai Besar TNKS.</li>
</ul>

<h2>Komposisi Pengunjung dan Pemandu</h2>
<ul>
   <li>Maksimal pemanduan: 1 orang pemandu untuk 5 orang pengunjung/pendaki.</li>
   <li>Maksimal beban porter: 1 orang porter seberat 25 kg.</li>
</ul>

<h2>Biaya</h2>
<p>Membayar karcis masuk sesuai dengan peraturan yang berlaku dan asuransi keselamatan.</p>

<h2>Pemeriksaan Petugas</h2>
<p>Petugas Balai Besar TNKS akan memeriksa barang bawaan, karcis, dan SIMAKSI sebelum dan sesudah memasuki
   kawasan. Dalam rangka penanggulangan sampah di Gunung Kerinci, para pendaki diwajibkan untuk meninggalkan
   salah satu identitas asli pribadi kepada petugas, yang akan dikembalikan apabila barang bawaan yang
   menghasilkan sampah dibawa kembali keluar TNKS.</p>